#include "./includes/libft.h"

int	ft_sqrt(int nb)
{
	int zm;

	zm = 1;
	if (nb == 0)
		return (0);
	while (zm * zm < nb)
		zm++;
	if ((zm * zm) == nb)
		return (zm);
	else
		return (0);
}
